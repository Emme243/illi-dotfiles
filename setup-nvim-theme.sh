AUTOCMD_BLOCK="$LUA_START_MARKER
-- Reload matugen colorscheme when matugen sends SIGUSR1.
-- Keep signal handle global so Lua GC does not collect it.
do
  local uv = vim.uv or vim.loop
  if uv and uv.new_signal then
    _G.matugen_reload_signal = _G.matugen_reload_signal or uv.new_signal()

    pcall(function()
      _G.matugen_reload_signal:start(\"sigusr1\", vim.schedule_wrap(function()
        pcall(vim.cmd.colorscheme, \"matugen\")
        vim.notify(\"Reloaded matugen colorscheme\", vim.log.levels.INFO)
      end))
    end)
  end
end
