write_file() {
	local path="$1"
	local content="$2"

	if [[ -f "$path" ]] && cmp -s "$path" <(printf '%s\n' "$content"); then
		echo "unchanged: $path"
		return
	fi

	printf '%s\n' "$content" >"$path"
	echo "wrote: $path"
}

patch_block() {
	local path="$1"
	local start="$2"
	local end="$3"
	local block="$4"

	touch "$path"

	if grep -qF "$start" "$path"; then
		awk -v start="$start" -v end="$end" -v block="$block" '
      BEGIN { in_block = 0 }
      index($0, start) {
        print block
        in_block = 1
        next
      }
      index($0, end) {
        in_block = 0
        next
      }
      !in_block { print }
    ' "$path" >"$path.tmp"
		mv "$path.tmp" "$path"
	else
		{
			cat "$path"
			printf '\n%s\n' "$block"
		} >"$path.tmp"
		mv "$path.tmp" "$path"
	fi

	echo "patched: $path"
}

